    padc, strfilter.
Aggregate: stdev, variance, mode, median, lower_quartile, upper_quartile.
*/
DROP TABLE IF EXISTS TEMP_TABLE_PHOPHORUS;

CREATE TABLE TEMP_TABLE_PHOPHORUS (
    'M. BASIN #' INTEGER UNIQUE
                        PRIMARY KEY
                        NOT NULL,
    'COUNT' INTEGER,
    'MIN' REAL,
    'Q1' REAL,
    'MEAN' REAL,
    'MEDIAN' REAL,
    'SE MEAN' REAL,
    'STDEV' REAL,
    'Q3' REAL,
    'MAX' REAL
);

/* STATEWIDE TOTAL */
INSERT INTO TEMP_TABLE_PHOPHORUS
SELECT
    0,
    COUNT(*) as 'COUNT',
    MIN(VALUE) AS 'MIN',
    LOWER_QUARTILE(VALUE) AS 'Q1',
    AVG(VALUE) AS 'MEAN',
    MEDIAN(VALUE) AS 'MEDIAN',
    STDEV(VALUE) / SQRT(COUNT(*)) AS 'SE MEAN',
    STDEV(VALUE) AS 'STDEV',
    UPPER_QUARTILE(VALUE) AS 'Q3',
    MAX(VALUE + 0) AS 'MAX'
FROM CHEMDATA
WHERE CHEMPARAMETER = 'Total Phosphorus'
;

